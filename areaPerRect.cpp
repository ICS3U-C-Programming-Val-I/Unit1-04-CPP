// This program calculates the area and perimeter
// of a rectangle with dimensions 8cm X 9cm

#include <iostream>

int main() {
    std::cout << "For a rectangle that has dimensions\n";
    std::cout << "length 8cm and width 9cm:\n";
    std::cout << "\n";
    std::cout << "Area = " << 8 * 9 << " cm^2\n";
    std::cout << "Perimeter = " << 2 * (8 + 9) << " cm\n";
}
